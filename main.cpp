struct LyricWord {
  String word;
  int delayTime;
};

void printWord(const String &word) {
  lcd.clearDisplay();
  
  // Convert to uppercase
  String upperWord = word;
  upperWord.toUpperCase();
  
  int16_t x1, y1;
  uint16_t w, h;
  lcd.getTextBounds(upperWord, 0, 0, &x1, &y1, &w, &h);
  
  int x = (lcd.width() - w) / 2;
  int y = (lcd.height() - h) / 2;
  
  lcd.setCursor(x, y);
  lcd.print(upperWord);
  lcd.display();
}

